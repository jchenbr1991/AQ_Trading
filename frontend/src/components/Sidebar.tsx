interface SidebarProps {
  collapsed: boolean;
  onToggle: () => void;
  mobileOpen: boolean;
  onMobileClose: () => void;
}

export function Sidebar({ collapsed, onToggle, mobileOpen, onMobileClose }: SidebarProps) {
  const location = useLocation();

  // Auto-close mobile drawer on navigation
  useEffect(() => {
    onMobileClose();
  }, [location.pathname]); // eslint-disable-line react-hooks/exhaustive-deps

  const isActive = (path: string) => {
    if (path === '/') return location.pathname === '/';
    return location.pathname.startsWith(path);
  };

  return (
    <>
      {/* Backdrop - mobile only */}
      {mobileOpen && (
        <div
          className="fixed top-14 left-0 right-0 bottom-0 bg-black/50 z-30 md:hidden"
          onClick={onMobileClose}
        />
      )}

      <aside
        className={`fixed top-14 left-0 bottom-0 bg-gray-900 text-gray-300 transition-all duration-200 z-40 flex flex-col w-60 ${
          mobileOpen ? 'translate-x-0' : '-translate-x-full'
        } md:translate-x-0 ${collapsed ? 'md:w-16' : 'md:w-60'}`}
      >
        {/* Toggle button - desktop only */}
        <button
          onClick={onToggle}
          className="hidden md:flex items-center justify-center h-10 mx-2 mt-2 mb-1 rounded hover:bg-gray-800 text-gray-400 hover:text-white transition-colors"
          title={collapsed ? 'Expand sidebar' : 'Collapse sidebar'}
        >
          <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.5}>
            {collapsed ? (
              <path strokeLinecap="round" strokeLinejoin="round" d="M8.25 4.5l7.5 7.5-7.5 7.5" />
            ) : (
              <path strokeLinecap="round" strokeLinejoin="round" d="M15.75 19.5L8.25 12l7.5-7.5" />
            )}
          </svg>
        </button>

        {/* Navigation */}
        <nav className="flex-1 overflow-y-auto px-2 pb-4">
          {NAV_GROUPS.map((group) => (
            <div key={group.title} className="mb-4">
              {!collapsed && (
                <div className="px-3 py-1 text-xs font-semibold text-gray-500 uppercase tracking-wider">
                  {group.title}
                </div>
              )}
              {collapsed && (
                <>
                  <div className="px-3 py-1 text-xs font-semibold text-gray-500 uppercase tracking-wider md:hidden">
                    {group.title}
                  </div>
                  <div className="border-t border-gray-700 mx-2 my-1 hidden md:block" />
                </>
              )}
              <ul className="space-y-0.5">
                {group.items.map((item) => {
                  const active = isActive(item.path);
                  return (
                    <li key={item.path}>
                      <Link
                        to={item.path}
                        title={collapsed ? item.label : undefined}
                        className={`flex items-center gap-3 px-3 py-2 rounded-md text-sm font-medium transition-colors ${
                          active
                            ? 'bg-blue-600/20 text-blue-400 border-l-2 border-blue-400'
                            : 'hover:bg-gray-800 hover:text-white'
                        } ${collapsed ? 'md:justify-center md:px-0' : ''}`}
                      >
                        <span className="flex-shrink-0">{item.icon}</span>
                        {collapsed ? (
                          <span className="md:hidden">{item.label}</span>
                        ) : (
                          <span>{item.label}</span>
                        )}
                      </Link>
                    </li>
                  );
                })}
              </ul>
            </div>
          ))}
        </nav>
      </aside>
    </>
  );
}
